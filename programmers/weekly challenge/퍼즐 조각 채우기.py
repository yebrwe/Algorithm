from collections import deque
def get_position(arr, pos, visited, k):
    result = [pos]
    dx = [0, 1, 0, -1]
    dy = [1, 0, -1, 0]

    n = len(arr)
    q = deque([pos])
    [px, py] = pos
    visited[px][py] = True
    while q:
        [x, y] = q.pop()
        for i in range(4):
            nx, ny = x+dx[i], y+dy[i]
            if 0<=nx<=n-1 and 0<=ny<=n-1 and not visited[nx][ny] and arr[nx][ny] == k:
                q.append([nx, ny])
                visited[nx][ny] = True
                result.append([nx, ny])
    return result


def to_zero(polygon):
    #top -1, 0
    flag = True
    while True:
        for [x, y] in polygon:
            if x-1 < 0: 
                flag = False
                break
        if not flag: break
        polygon = [[x-1, y] for [x,y] in polygon]
    #left 0, -1
    flag = True
    while True:
        for [x, y] in polygon:
            if y-1 < 0: 
                flag = False
                break
        if not flag: break
        polygon = [[x, y-1] for [x,y] in polygon]
    return polygon

def get_max_width(polygon):
    k=-1
    for [x, y] in polygon:
        k = max(k, max(x, y))
    return k+1


def rotate90(arr):
    n = len(arr)
    tmp = [ [0] * n for _ in range(n)]
    for i in range(n):
        for j in range(n):
            tmp[i][j] = arr[n-1-j][i]
    sum_w = 0
    sum_h = 0
    while sum_w ==0 or sum_h ==0:
        for i in range(n):
            sum_w += tmp[0][i]
        for i in range(n):
            sum_h += tmp[i][0]
        #left
        if sum_h == 0:
            for i in range(n):
                for j in range(n):
                    if j+1 > n-1: continue
                    tmp[i][j], tmp[i][j+1] = tmp[i][j+1], tmp[i][j]
        #top
        if sum_w == 0:
            for i in range(n):
                for j in range(n):
                    if i+1 > n-1: continue
                    tmp[i][j], tmp[i+1][j] = tmp[i+1][j], tmp[i][j]
    return tmp
    
def copy_board(n, polygon):
    board = [ [0] * n for _ in range(n)]
    for [x,y] in polygon:
        board[x][y] = 1
    return board

